# 求n的阶乘：t = 1 × 2 × …… × n  最后将求得结果存放在变量t中

import sys
if len(sys.argv) < 2:
    n = 5  # 设置n的默认值为5
else:
    n = int(sys.argv[1])

#------------------------------------
#（请在两条虚线之间填写代码实现指功能，并将结果存放在变量t中）
print (dir(sys))
t=1
for m in range(1,n+1):
    t*=m
#------------------------------------
print('结果为：',t)
